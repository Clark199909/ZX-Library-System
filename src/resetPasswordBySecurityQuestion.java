    private void checkButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkButtonActionPerformed
        StringBuilder emailSb = new StringBuilder(emailInput.getText());
        email = UpdateData.dealSpcialSymbols(emailSb);
        ResultSet rs = Select.getData("select *from users where email='"+email+"'");
        try{
            if(Email.verifyEmailFormat(email) && rs.next()){
                securityQuestion = rs.getString(4);
                securityQuestionData.setText(securityQuestion);
            }else{
                JOptionPane.showMessageDialog(null, "Email is invalid or not registered!");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_checkButtonActionPerformed

