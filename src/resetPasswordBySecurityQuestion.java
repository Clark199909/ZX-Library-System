    private void resetPasswordButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetPasswordButtonActionPerformed
        StringBuilder answerSb = new StringBuilder(answerInput.getText());
        String answer = UpdateData.dealSpcialSymbols(answerSb);
        StringBuilder newPasswordSb = new StringBuilder(newPasswordInput.getText());
        String newPassword = UpdateData.dealSpcialSymbols(newPasswordSb);
        StringBuilder newPasswordRepeatSb = new StringBuilder(repeatNewPasswordInput.getText());
        String newPasswordRepeat = UpdateData.dealSpcialSymbols(newPasswordRepeatSb);
        if(email.equals("") || answer.equals("") || newPassword.equals("") || newPasswordRepeat.equals("")){
            JOptionPane.showMessageDialog(null, "All field is required.");
        }else if(!newPassword.equals(newPasswordRepeat)){
            JOptionPane.showMessageDialog(null, "Need to repeat the same password!");
        }else{
            ResultSet rs=Select.getData("select *from users where email='"+email+"' and answer='"+answer+"'");
            try{
                if(rs.next()){
                    if(rs.getString(3).equals(newPassword)){
                        JOptionPane.showMessageDialog(null, "Entered the old password, need to enter a new one!");
                    }else{
                        UpdateData.setData("update users set password='"+newPassword+"' where email='"+email+"'", "Password set successfully!");
                        setVisible(false);
                        new Login().setVisible(true);
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Answer to the security question is wrong!");
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_resetPasswordButtonActionPerformed

